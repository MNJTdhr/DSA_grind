#include <iostream>
using namespace std;

int main()
{
    cout << sizeof(int) << endl;            //== 4bytes
    cout << sizeof(long int) << endl;       //>= 4bytes
    cout << sizeof(short int) << endl;      //== 2bytes
    cout << sizeof(long long int) << endl;  //== 8bytes
    cout << sizeof(int) << endl;
    cout << sizeof(int) << endl;

    return 0;
}